using System; // Підключення простору імен System

class Task_7 // Оголошення класу Task_7
{
    static void Main() // Головний метод програми
    {
        Console.InputEncoding = System.Text.Encoding.UTF8; // Встановлення кодування для введення UTF-8
        Console.OutputEncoding = System.Text.Encoding.UTF8; // Встановлення кодування для виведення UTF-8

        Console.Write("Введіть розмір матриці: "); // Виведення запиту на введення розміру матриці
        int size = int.Parse(Console.ReadLine()); // Зчитування розміру матриці з консолі та перетворення в ціле число

        int[][] matrix = new int[size][]; // Оголошення зубчастого масиву (матриці)
        Random rand = new Random(); // Створення об'єкта для генерації випадкових чисел

        // Ініціалізація зубчастого масиву
        for (int i = 0; i < size; i++) // Цикл для ініціалізації кожного рядка матриці
        {
            matrix[i] = new int[size]; // Ініціалізація рядка матриці
        }

        // Заповнення матриці випадковими числами
        for (int i = 0; i < size; i++) // Цикл для проходження по рядках матриці
        {
            for (int j = 0; j < size; j++) // Цикл для проходження по стовпцях матриці
            {
                matrix[i][j] = rand.Next(0, 100); // Заповнення елемента матриці випадковим числом від 0 до 99
            }
        }

        // Відображення верхньої половини на нижню
        for (int i = 0; i < size / 2; i++) // Цикл для проходження по верхній половині матриці
        {
            for (int j = 0; j < size; j++) // Цикл для проходження по стовпцях матриці
            {
                matrix[size - 1 - i][j] = matrix[i][j]; // Копіювання елементів верхньої половини в нижню
            }
        }

        // Виведення матриці
        for (int i = 0; i < size; i++) // Цикл для проходження по рядках матриці
        {
            for (int j = 0; j < size; j++) // Цикл для проходження по стовпцях матриці
            {
                Console.Write(matrix[i][j] + "\t"); // Виведення елемента матриці з табуляцією
            }
            Console.WriteLine(); // Перехід на новий рядок після виведення рядка матриці
        }

        Console.ReadKey(); // Очікування натискання клавіші перед завершенням програми
    }
}

