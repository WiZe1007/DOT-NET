using System; // Підключення простору імен System для використання базових функцій

class Task1 // Оголошення класу Task1
{
    static void Main() // Основний метод програми, точка входу
    {
        // Оголошення масивів для зберігання даних про транспорт
        string[] transportType = new string[3]; // Масив для типу транспорту (трамвай, тролейбус, автобус)
        string[] routeNumber = new string[3];  // Масив для номерів маршрутів
        double[] routeLength = new double[3];  // Масив для довжини маршрутів (в кілометрах)
        int[] travelTime = new int[3];         // Масив для часу в дорозі (в хвилинах)

        // Цикл для введення даних від користувача
        for (int i = 0; i < 3; i++) // Виконується 3 рази, для кожного транспорту
        {
            Console.WriteLine($"Введіть дані для транспорту {i + 1}:"); // Запит на введення даних для кожного транспорту

            Console.Write("Вид транспорту (Тр, Тл, А): "); // Запит на введення типу транспорту
            transportType[i] = Console.ReadLine(); // Зчитування введеного типу транспорту

            Console.Write("Номер маршруту: "); // Запит на введення номера маршруту
            routeNumber[i] = Console.ReadLine(); // Зчитування номера маршруту

            Console.Write("Протяжність маршруту (км): "); // Запит на введення довжини маршруту
            routeLength[i] = Convert.ToDouble(Console.ReadLine()); // Перетворення введеного значення на число типу double

            Console.Write("Час в дорозі (хвилин): "); // Запит на введення часу в дорозі
            travelTime[i] = Convert.ToInt32(Console.ReadLine()); // Перетворення введеного значення на ціле число

            Console.WriteLine(); // Виведення порожнього рядка для візуального розділення введень
        }

        // Виведення заголовка таблиці
        Console.WriteLine("-------------------------------------------------------------");
        Console.WriteLine("|                Відомості суспільного транспорту           |");
        Console.WriteLine("-------------------------------------------------------------");

        // Виведення заголовків стовпців таблиці
        Console.WriteLine("| Вид транспорту | № маршруту | Протяжність (км) | Час (хв) |");
        Console.WriteLine("|----------------|------------|------------------|----------|");

        // Цикл для виведення даних про кожен транспорт
        for (int i = 0; i < transportType.Length; i++) // Проходить по всіх елементах масивів
        {
            // Виведення даних у форматі таблиці
            Console.WriteLine($"| {transportType[i],-14} | {routeNumber[i],-10} | {routeLength[i],-16} | {travelTime[i],-8} |");
        }

        // Виведення примітки з роз'ясненнями про типи транспорту
        Console.WriteLine("-------------------------------------------------------------");
        Console.WriteLine("| Примітка: Тр – трамвай, Тл – тролейбус, А – автобус       |");
        Console.WriteLine("-------------------------------------------------------------");
    }
}

